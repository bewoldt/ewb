                <h2 class="text-center">Community Info</h2>
                <img src="images/hr.png">
                <p class="lead">The Kobriti village is located about 20 minutes from the town of Ejura by road. It lies in a heavily wooded area. The population is approximately 500 individuals, in 70 households living on 0.4 square kilometers of land.</p>
                <p>The population consists mostly of migrants – predominantly Muslim farmers - from the Northern regions of Ghana. While some of the villagers maintain their northern dialects, a vast majority are proficient in Twi, the language of the Ashanti region. Only a small percentage of the village is proficient in English, a majority of whom are teachers at the school.</p>

                <p>The water in this community comes from two centrally located boreholes. Both boreholes tested negative for E. Coli presence in three different tests, in two different seasons of the year. The boreholes also tested negative for Arsenic. In this area, the main crops are plantain, cassava, yam, watermelon and maize. Most of the farmers are substance farmers, growing only what their family can eat. Most of the farming is done by hand, without the use of machinery or heavy equipment.</p>
                <p>The village does not currently have electricity, but plans to connect the village are being made by the District authorities. The buildings are mostly made of mud, some with an internal wood frame made from tree branches. The roofs are primarily thatch, although some of the families have tin roofs. Currently about 130 students attend the Kobriti primary school, including students from Kobriti as well as the sister village. There are paved roads leading the entire way from this village to the district capitol of Ejura. The village is most assessable on Mondays when Ejura holds its market day.</p>
                <p>The community is also located along some of the public transport routes and it is assessable by taxi. Located in Ejura is a health clinic that can provide health care, although it is limited in size and located 30 minutes by car from Kobriti. There is a smaller health clinic that is located in Sekyedumase, only 9 km from Kobriti. While these clinics exist, many people are reluctant to use them because they do not have health insurance. Health insurance cost GH¢18.00 for adults and GH¢4.00 for children (US$ 13.00 and US$3.00 respectively). While these costs seem small, they can be quite a lot for a farmer in this region.</p>
